<?php
class MyCalculator{
    function add($x, $y) {
        echo "Addition is: ".($x + $y). "<br>";
    }
    function sub($x, $y) {
        echo "Substraction is: ".($x - $y). "<br>";
    }
    function mul($x, $y) {
        echo "Multiplication is: ".($x * $y). "<br>";
    }
    function div($x, $y) {
        echo "Division is: ".($x / $y). "<br>";
    }
    function mod($x, $y) {
        echo "Modulo is: ".($x % $y). "<br>";
    }

}


 ?>
